package LaGuaridadelDragon;

/**
 *
 * @author nn
 */
public class Tesoro {
    
    private int valor;
    private String descrip;

    public Tesoro(int valor, String descrip) {
        this.valor = valor;
        this.descrip = descrip;
    }

    public int verValor() {
        return valor;
    }

    public String verDescripcion() {
        return descrip;
    }
}

   

